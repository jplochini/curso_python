## CRIANDO LISTAS NUMERICAS

# Usando a funcao range()
# A funcao range() exibe uma sequencia de numeros

for value in range(1,5):
    print( value )

# Usando range() para criar uma lista de numeros

numeros = list(range(1,6)) # A funcao list() coloca a sequencia de numeros gerados por range() em uma lista
print ( numeros )


